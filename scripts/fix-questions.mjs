// ---------------------------------------------------------------------------
// 1) Global niqqud / spelling sweep applied to every prompt and option string.
//    Only unambiguous, systematic corrections live here.
// ---------------------------------------------------------------------------
const SWEEP = [
  [/לֹּא/g, 'לֹא'],            // dagesh wrongly placed in the lamed of the negation
  [/מַעֲרִיר/g, 'מְעוֹרֵר'],     // "arouses" – wrong vocalization/spelling
  [/מַעֲרֵר/g, 'מְעוֹרֵר'],
  [/טְעוּתָה/g, 'טָעוּת'],       // non-word; correct noun is טָעוּת
  [/חֲיָובִית/g, 'חִיּוּבִית'],   // "positive" – wrong vocalization
];
function sweep(s) {
  let out = s;
  for (const [re, to] of SWEEP) out = out.replace(re, to);
  return out;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------
function quotedWord(prompt) {
  const m = prompt.match(/[\u05F4\u201C\u201D"]([^\u05F4\u201C\u201D"]+)[\u05F4\u201C\u201D"]/);
  return m ? m[1] : null;
}

// Replace the whole question at idx with explicit fields.
function patch(idx, fields) {
  const q = data[idx];
  if (!q) throw new Error('no question at ' + idx);
  if (fields.prompt !== undefined) q.prompt = fields.prompt;
  if (fields.options !== undefined) q.options = fields.options;
  if (fields.correctIndex !== undefined) q.correctIndex = fields.correctIndex;
}

// Replace only the correct option's text (keeps position & distractors).
function setCorrect(idx, text) {
  const q = data[idx];
  q.options[q.correctIndex] = text;
}

// Keep the correct option, replace the three distractors (comparable length).
function enrich(idx, distractors) {
  const q = data[idx];
  if (distractors.length !== 3) throw new Error('need 3 distractors at ' + idx);
  const correct = q.options[q.correctIndex];
  q.options = [correct, ...distractors];
  q.correctIndex = 0;
}

// Convert a synonym-list answer into an "odd one out" question.
//   syns: 3 real synonyms of the prompt word.
//   wrong: a hard word that is NOT a synonym (the correct answer).
const GERSH = '\u05F4'; // ״
function oddOneOut(idx, syns, wrong) {
  const q = data[idx];
  const word = quotedWord(q.prompt) || q.prompt;
  if (syns.length !== 3) throw new Error('need 3 syns at ' + idx);
  q.type = 'trivia'; // full-prompt question, like the existing "odd one out" items
  q.prompt = `אֵיזוֹ מִילָה אֵינָהּ נִרְדֶּפֶת לְ${GERSH}${word}${GERSH}?`;
  q.options = [...syns, wrong];
  q.correctIndex = 3;
}

// Point correctIndex at the option whose stripped text matches `text`.
function setCorrectIndexTo(idx, text) {
  const q = data[idx];
  const j = q.options.indexOf(text);
  if (j < 0) throw new Error('option not found at ' + idx + ': ' + text);
  q.correctIndex = j;
}

